    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        
        int result, opcao;
        String sql;
        Object[] options = {"Sim", "Não"};
        opcao = JOptionPane.showOptionDialog(null,"Você Confirma a exclusão?","Exclusão", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, options,options[1]);
        jbExcluir.setEnabled(false);
        JbConsul.setEnabled(false);
        jbSalvar.setEnabled(false);
        
        if(opcao == 0){
            
            try{
                sql = "delete from consulta where nome = ?";
                PreparedStatement stm = con.prepareStatement(sql,
                    ResultSet.TYPE_SCROLL_SENSITIVE,
                    ResultSet.CONCUR_UPDATABLE
                    );
                stm.setString(1, TxNome.getText());
                
                result = stm.executeUpdate();
                if(result>0){
                    boolean resultado = stm.execute();
                    System.out.println(resultado);
                    if(stm.execute()==false){
                        JOptionPane.showMessageDialog(null, "Dados excluídos com sucesso!");
                     
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Erro ao excluir!");
                    }
                    
                }
            }catch(SQLException e)
            {
                    JOptionPane.showMessageDialog(null, "Erro!" + e);
            }
        }
        
        
        
        
        
    }//GEN-LAST:event_jbExcluirActionPerformed

    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed

        int result;
        String sql, Nome, CPF, Telefone, Cidade, CEP, RG, Endereço, Pais, Celular, Estado;

        Nome = TxNome.getText();
        CPF = TxCpf.getText();
        Telefone = TxTel.getText();
        Cidade = TxCidade.getText();
        CEP = TxCpf.getText();
        RG = TxRg.getText();
        Endereço = TxEndereco.getText();
        Pais  = TxPais.getText();
        Celular = TxCel.getText();
        Estado = TxEstado.getText();

        try{
            sql = "insert into consulta( Nome, CPF, Telefone, Cidade, CEP, RG, Endereço, Pais, Celular, Estado) values(?,?,?,?,?,?,?,?,?,?)";
            PreparedStatement stm = con.prepareStatement(sql);

            stm.setString(1, Nome);
            stm.setString(2, CPF);
            stm.setString(3, Telefone);
            stm.setString(4, Cidade);
            stm.setString(5, CEP);
            stm.setString(6, RG);
            stm.setString(7, Endereço);
            stm.setString(8, Pais);
            stm.setString(9, Celular);
            stm.setString(10, Estado);

            TxNome.setText("");
            TxCpf.setText("");
            TxTel.setText("");
            TxCidade.setText("");
            TxCep.setText("");
            TxRg.setText("");
            TxEndereco.setText("");
            TxPais.setText("");
            TxCel.setText("");
            TxEstado.setText("");
            TxNome.requestFocus();

            result = stm.executeUpdate();
            if(result > 0)
            {
                JOptionPane.showMessageDialog(null, "Dados Gravados com Sucesso");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Erro ao inserir dados");
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Erro!" + e);
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

