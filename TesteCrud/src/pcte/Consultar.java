    private void JbConsulActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbConsulActionPerformed
        String usuario = TxNome.getText();

        try{
            String sql = "SELECT * FROM consulta WHERE nome like ?";
            PreparedStatement stm = con.prepareStatement(sql);
            stm.setString(1,"%"+usuario+"%");
            ResultSet rs = stm.executeQuery();

            if(rs.next()){
                TxNome.setText(rs.getString("Nome"));
                TxCpf.setText(rs.getString("CPF"));
                TxTel.setText(rs.getString("Telefone"));
                TxCidade.setText(rs.getString("Cidade"));
                TxCep.setText(rs.getString("CEP"));
                TxRg.setText(rs.getString("RG"));
                TxEndereco.setText(rs.getString("Endereço"));
                TxPais.setText(rs.getString("Pais"));
                TxCel.setText(rs.getString("Celular"));
                TxEstado.setText(rs.getString("Estado"));
                
            
            }
            else{
                JOptionPane.showMessageDialog(null,"Usuario não encontrado!", null, 3);
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(this, "Erro ao conectar:"+ ex.getMessage());
        }

    }//GEN-LAST:event_JbConsulActionPerformed

