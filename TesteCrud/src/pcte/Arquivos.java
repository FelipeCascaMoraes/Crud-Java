    private void BtnProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnProcurarActionPerformed
        File arquivo;
        String nome_arq = null;
        BufferedImage imagem;
        
        
        FC.setFileFilter(
        new FileNameExtensionFilter(
        "Arquivo de imagem(JPG, PNG, PDF, TXT)",
        "png","jpg","txt","pdf")
                );
        
        FC.setAcceptAllFileFilterUsed(false);
        
        if (FC.showOpenDialog(null)==0){
            arquivo = FC.getSelectedFile();
            txtDiretorio.setText(nome_arq);
            try{
                imagem = ImageIO.read(arquivo);
                ImageIcon icon = new ImageIcon(
                imagem.getScaledInstance(200, 200, java.awt.Image.SCALE_SMOOTH));
                foto.setIcon(icon);
            }catch(IOException e){
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_BtnProcurarActionPerformed

    private void BtnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEnviarActionPerformed
        try{
        FTPClient ftp = new FTPClient();
        ftp.connect("127.0.0.1");
        ftp.login("Teste","1234");
        ftp.setFileType(FTPClient.BINARY_FILE_TYPE);
        
        FileInputStream arqEnviar = new FileInputStream(txtUrl.getText());
         if(ftp.storeFile(txtDiretorio.getText(),arqEnviar)){
             System.out.println("Arquivo armazenado com sucesso");
         }else{
             System.out.println("Erro ao armazenar o arquivo!");
         }
        }
        catch(IOException e){
            System.out.println("Erro ao armazenar o arquivo"); 
        }
        
    }//GEN-LAST:event_BtnEnviarActionPerformed


